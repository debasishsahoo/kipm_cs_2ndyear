let a = 10;
let b = 3;

console.log(a + b); // 13 (addition)
console.log(a - b); // 7 (subtraction)
console.log(a * b); // 30 (multiplication)
console.log(a / b); // 3.333... (division)
console.log(a % b); // 1 (modulus - remainder)
console.log(a ** b); // 1000 (exponentiation)
console.log(++a); // 11 (pre-increment)
console.log(--b); // 2 (pre-decrement)
console.log(a++); // 11 (post-increment, a becomes 12 after this line)
console.log(b--); // 2 (post-decrement, b becomes 1 after this line)

console.log("5 / 0 =", 5 / 0);
console.log("0 / 0 =", 0 / 0);
console.log("Even check (a % 2 === 0):", a % 2 === 0);
console.log("Odd check (b % 2 !== 0):", b % 2 !== 0);
console.log("Negative check (a < 0):", a < 0);
console.log("Positive check (b > 0):", b > 0);
